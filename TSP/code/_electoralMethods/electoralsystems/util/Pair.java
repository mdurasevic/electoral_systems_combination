package _electoralMethods.electoralsystems.util;

class Pair {
        private double first;
        private int second;

        public Pair(double first, int second) {
            this.first = first;
            this.second = second;
        }

        public double getFirst() {
            return first;
        }

        public int getSecond() {
            return second;
        }
    }